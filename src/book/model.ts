import mongoose from "mongoose";
import { BookModelInterface } from "@lib/interfaces/books/bookModel";

const bookSchema = new mongoose.Schema<BookModelInterface>({
  title: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  author: {
    type: String,
    required: true,
  },
  genre: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  stock: {
    type: Number,
    required: true,
  },
});

export const BookModel = mongoose.model("Book", bookSchema);
